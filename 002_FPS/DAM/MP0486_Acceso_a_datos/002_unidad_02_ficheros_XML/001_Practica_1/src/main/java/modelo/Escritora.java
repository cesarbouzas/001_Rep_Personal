public class Escritora {
private String name;
 private int fechNac;

    public Escritora(String name, int fechNac) {
        this.name = name;
        this.fechNac = fechNac;
    }

    public String getName() {
        return name;
    }

    public int getFechNac() {
        return fechNac;
    }
 
 
 
}
